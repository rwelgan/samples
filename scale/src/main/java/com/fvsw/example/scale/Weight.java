package com.fvsw.example.scale;

/**
 * @author rwelgan
 * 
 * An item that can go on the scale is a weight.  The weight has only 
 * a mass.
 *
 */
public class Weight {

	int mass;

	/**
	 * @return the mass of the weight
	 */
	public int getMass() {
		return mass;
	}

	/**
	 * @param mass the weight's mass to set
	 */
	public void setMass(int mass) {
		this.mass = mass;
	} 
	
	
}
